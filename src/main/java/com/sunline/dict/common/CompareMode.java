package com.sunline.dict.common;

/**
 * 比对模式枚举
 */
public enum CompareMode {
    
    /**
     * 普通Excel比对模式
     * - 比对所有列
     * - 完全匹配
     */
    NORMAL,
    
    /**
     * XML模型与数据库比对模式
     * - 只比对A、B、D、F、G列
     * - D列应用类型等价规则（date=timestamp, numeric=decimal）
     * - G列去除::后缀后比对
     * - 修订记录改名为"差异结果"
     */
    XML_DB,
    
    /**
     * ESF接口文档比对模式
     * - 识别输入/输出两个部分
     * - 只比对A、B、C、D列（英文名、中文名、数据类型、是否必输）
     * - 按交易码汇总差异到修订记录
     * - 修订记录格式：序号、交易码、新增/删除/修改、修订内容
     */
    ESF_INTERFACE,
    
    /**
     * 迁移中间表比对模式
     * - 左边：XML表定义文档，右边：迁移中间表文档
     * - 跳过"表总览"sheet
     * - 字段部分特殊映射（XML的A/B/C/D/F/H列 -> 结果的B/C/D/E/F/H列）
     * - 结果A列从1开始递增
     * - 字段修改：黄色显示最新值（不显示旧值->新值）
     * - 字段新增（左有右无）：绿色显示
     * - 字段删除（左无右有）：在修订记录中体现
     * - 修订记录格式：表名、修订级别、修订方式、修订明细（带双向超链接）
     */
    MIGRATION_TABLE
}

